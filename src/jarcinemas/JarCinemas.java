    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:
        try {
            data.beforeFirst();
            boolean isValid = database.executeQuery("select * from loginusers where username='"+usernameLogin.getText()+"' and password='"+String.valueOf(passwordLogin.getPassword())+"'");
            if(isValid){
                ResultSet rs = database.getResultSet("select * from loginusers where username='"+usernameLogin.getText()+"'");
                rs.next();
                UserInfo user = new UserInfo(rs.getString("name"),usernameLogin.getText(),rs.getInt("type"));
                (new Home()).setVisible(true);
                this.dispose();
            }
            else (new popup(this,"Invalid username or password")).setVisible(true);
        } catch (SQLException ex) {
            (new popup(this,ex.getMessage())).setVisible(true);
        }
        usernameLogin.setText("username");
        usernameLogin.setForeground(new Color(153,153,153));
    }//GEN-LAST:event_loginButtonMouseClicked

