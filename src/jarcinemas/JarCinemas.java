    private void signupButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signupButtonMouseClicked
        try {
            boolean isValid = false;
            data.beforeFirst();
            while(data.next()){
                if(data.getString("username").equals(usernameSignup.getText()))
                    isValid = true;
            }
            if(String.valueOf(passwordSignup.getPassword()).length()<7||nameSignup.getText().length()==0||String.valueOf(passwordSignup.getPassword()).equals("Your Name")||nameSignup.getText().equals("Your Password")
                    ||usernameSignup.getText().equals("Preffered Username")||usernameSignup.getText().length()==0)
                isValid = true;
            // TODO add your handling code here:
            if(!isValid && database.executeQuery("INSERT INTO loginusers VALUES ('"+usernameSignup.getText()+"','"+String.valueOf(passwordSignup.getPassword())+"','"+nameSignup.getText()+"',1)")){
                {
                    (new popup(this,"Successfully registered your account")).setVisible(true);
                    toggleLogin();
                }
                try {
                    data = database.getResultSet("SELECT * FROM loginusers");
                } catch (SQLException ex) {
                    (new popup(this,"Error retrieving user data")).setVisible(true);
                    return;
                }
            }
            else
                (new popup(this,"Failed to register your account")).setVisible(true);
                } catch (SQLException ex) {
            Logger.getLogger(JarCinemas.class.getName()).log(Level.SEVERE, null, ex);
        }
        usernameSignup.setText("Preffered Username");
        usernameSignup.setForeground(new Color(153,153,153));
        
        passwordSignup.setEchoChar((char)0);
        passwordSignup.setForeground(new Color(153,153,153));
        passwordSignup.setText("Your Password");
        
        nameSignup.setText("Your Name");
        nameSignup.setForeground(new Color(153, 153, 153));
    }//GEN-LAST:event_signupButtonMouseClicked

