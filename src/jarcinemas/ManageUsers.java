    private void usernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_usernameFocusLost
        // TODO add your handling code here:
        if(username.getText().equals("")){
            try {
                username.setText(data.getString("username"));
            } catch (SQLException ex) {
                Logger.getLogger(ManageUsers.class.getName()).log(Level.SEVERE, null, ex);
            }
            username.setForeground(new Color(153,153,153));
        }
        else{
            try {
                SQLConnect database = new SQLConnect();
                if(database.executeQuery("SELECT * from loginusers where username='"+username.getText()+"'")){
                    (new popup(this,"This username is in use!")).setVisible(true);
                    username.setText(data.getString("username"));
                    username.setForeground(new Color(153,153,153));
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ManageUsers.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ManageUsers.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_usernameFocusLost

