    private void BookingsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BookingsMouseClicked
        try {
            // TODO add your handling code here:
            SQLConnect databse = new SQLConnect();
            if(!databse.executeQuery("select * from booking where username='"+(new UserInfo()).username()+"'"))
                (new popup((new Home()),"There are no existing booking under this name!")).setVisible(true);
            else{
                (new Bookings(originalwidth,originalheight)).setVisible(true);
                this.dispose();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AccountMenu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AccountMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BookingsMouseClicked

