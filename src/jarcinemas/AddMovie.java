    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        try {
            // TODO add your handling code here:
            if((new SQLConnect()).executeQuery("insert into movies values("+Integer.toString(count)+", '"+name.getText()+"', '"+description.getText()+"')")){
                (new popup(this,"New Movie Added!")).setVisible(true);
                (new ManageMovies()).setVisible(true);
                this.dispose();
            }
            else
                (new popup(this,"Something went wrong!")).setVisible(true);
        } catch (ClassNotFoundException ex) {
            (new popup(this,"Something went wrong!")).setVisible(true);
        } catch (SQLException ex) {
            (new popup(this,"Something went wrong!")).setVisible(true);
        }
    }//GEN-LAST:event_addMouseClicked

