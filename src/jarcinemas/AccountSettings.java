    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        update.requestFocus();
        String query = "UPDATE loginusers SET ";
        if (ch_un) {
            try {
                SQLConnect database = new SQLConnect();
                database.executeQuery("UPDATE booking set username=NULL WHERE username='"+user.username()+"'");
            } catch (SQLException ex) {
                Logger.getLogger(AccountSettings.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AccountSettings.class.getName()).log(Level.SEVERE, null, ex);
            }
            query = query + "username='" + username.getText() + "'";
        }
        if (ch_n) {
            if(ch_un)
                query = query + ",";
            query = query + "name='" + name.getText() + "'";
        }
        if (ch_pass){
            if(ch_un | ch_n)
                query = query + ",";
            query = query + "password='" + password.getText() + "'";
        }
        query = query + "WHERE username='" + user.username() + "'";
        try {
            SQLConnect database = new SQLConnect();
            if(database.executeQuery(query)){
                (new popup(this,"Update Successful!")).setVisible(true);
                if (ch_un){
                    user.setUser(username.getText());
                    database.executeQuery("UPDATE booking set username='"+user.username()+"' WHERE username is NULL");
                }
                if (ch_n) user.setName(name.getText());
            }
            else
                (new popup(this,"Looks like something went wrong!")).setVisible(true);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AccountSettings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AccountSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateMouseClicked

