    private void usernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_usernameFocusLost
        // TODO add your handling code here:
        if(username.getText().equals(""))
            username.setText("Your User Name: "+user.username());
        try {
            // TODO add your handling code here:
            SQLConnect database = new SQLConnect();
            if(username.getText().equals("Your User Name: "+user.username()))
                ch_un = false;
            else if(database.executeQuery("SELECT * FROM loginusers where username='"+username.getText()+"'")){
                (new popup(this,"This username is already in use")).setVisible(true);
                username.setText(user.username());
                ch_un = false;
            }
            else 
                ch_un = true;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AccountSettings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AccountSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_usernameFocusLost

